void button_and_rotation(void) {

	if (i != 1) {
		if (HAL_GPIO_ReadPin(up_GPIO_Port, up_Pin)) {
			i = 2;
		}
	}
	if (i != 2) {
		if (HAL_GPIO_ReadPin(down_GPIO_Port, down_Pin)) {
			i = 1;
		}
	}
	if (i != 3) {
		if (HAL_GPIO_ReadPin(left_GPIO_Port, left_Pin)) {
			i = 4;
		}
	}
	if (i != 4) {
		if (HAL_GPIO_ReadPin(right_GPIO_Port, right_Pin)) {
			i = 3;
		}
	}

	switch (i) {
	case 1:
		y++;
		break;
	case 2:
		y--;
		break;
	case 3:
		x--;
		break;
	case 4:
		x++;
		break;
	}
}
